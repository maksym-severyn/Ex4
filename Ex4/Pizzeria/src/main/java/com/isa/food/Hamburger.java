package com.isa.food;

public class Hamburger extends Food{
    private String name;

    public Hamburger(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
